using System;
using static System.Console;

namespace classlib
{
    public class Suma 
    {   
        public Suma()
        {}
        public int sumame(int num1, int num2)
        {
            return num1 + num2;
        }

        public int sumame(int num1, int num2, int num3)
        {
            return num1 + num2 + num3;
        }
    }
}        